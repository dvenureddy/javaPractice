package basics;

public class LoopsConcept {

	public static void main(String[] args) {
		// 1. While loop
		// 2. For loop
		
		int i=1; // Initialize 		
		while(i<5) { // Condition
			System.out.println("I Value is : " + i);
			i++; // increment or decrement 
		}
		
		System.out.println("-------------------------");
		
		for(int j=10;j<15;j++) { 
			System.out.println("J Value is : " + j);
		}

	}

}
